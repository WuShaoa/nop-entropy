package io.nop.api.core.beans.graphql;

import io.nop.api.core.annotations.data.DataBean;

import java.io.Serializable;

@DataBean
public class GraphQLSourceLocation implements Serializable {
    private static final long serialVersionUID = -3308851977519953935L;

    private int line;
    private int column;

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}