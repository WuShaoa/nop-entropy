package io.nop.api.core.audit;

import io.nop.api.core.annotations.data.DataBean;
import io.nop.api.core.auth.IUserContext;
import io.nop.api.core.beans.ExtensibleBean;

import java.sql.Timestamp;

@DataBean
public class AuditRequest extends ExtensibleBean {
    private String operation;
    private String description;
    private String entityId;

    private String retMessage;

    // 请求数据
    private String requestData;

    // 结果数据
    private String responseData;

    private String errorCode;
    private int resultStatus;

    private String userName;

    private String userId;
    private String clientId;
    private String appId;

    private Timestamp actionTime;
    private String sessionId;
    private long usedTime;

    private String tenantId;

    public static AuditRequest newRequest(String operation, String entityId, IUserContext userContext) {
        AuditRequest req = new AuditRequest();
        req.setOperation(operation);
        req.setEntityId(entityId);
        req.setUserName(userContext.getUserName());
        req.setSessionId(userContext.getSessionId());
        req.setTenantId(userContext.getTenantId());
        return req;
    }

    public String getTenantId() {
        return tenantId;
    }

    public void setTenantId(String tenantId) {
        this.tenantId = tenantId;
    }

    public long getUsedTime() {
        return usedTime;
    }

    public void setUsedTime(long usedTime) {
        this.usedTime = usedTime;
    }

    public String getSessionId() {
        return sessionId;
    }

    public void setSessionId(String sessionId) {
        this.sessionId = sessionId;
    }

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getEntityId() {
        return entityId;
    }

    public void setEntityId(String entityId) {
        this.entityId = entityId;
    }

    public String getRequestData() {
        return requestData;
    }

    public void setRequestData(String requestData) {
        this.requestData = requestData;
    }

    public String getResponseData() {
        return responseData;
    }

    public void setResponseData(String responseData) {
        this.responseData = responseData;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public int getResultStatus() {
        return resultStatus;
    }

    public void setResultStatus(int resultStatus) {
        this.resultStatus = resultStatus;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getAppId() {
        return appId;
    }

    public void setAppId(String appId) {
        this.appId = appId;
    }

    public Timestamp getActionTime() {
        return actionTime;
    }

    public void setActionTime(Timestamp actionTime) {
        this.actionTime = actionTime;
    }

    public String getRetMessage() {
        return retMessage;
    }

    public void setRetMessage(String retMessage) {
        this.retMessage = retMessage;
    }
}